# Modulo (remainder) %
print(10 % 3)
print('--------------')


# Equals ==
print(10 == 0)
print(10 == 10) # 10 = 10 Be careful of this!
print('--------------')


# Greater than >
print(10 > 0)
print(10 >= 10)
print('--------------')


# Less than <
print(10 < 0)
print(10 <= 10)
print('--------------')
